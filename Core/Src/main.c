/* USER CODE BEGIN 0 */
void selectMuxPin(uint8_t pin) {
	for (uint8_t j = 0; j < 3; j++) {
		if (pin & (1 << j)) {
			if (j == 2) {
				HAL_GPIO_WritePin(MGO, selectPins[j], GPIO_PIN_SET);
			} else {
				HAL_GPIO_WritePin(MGO, selectPins[j], GPIO_PIN_SET);
			}
		} else {
			if (j == 2) {
				HAL_GPIO_WritePin(MGO, selectPins[j], GPIO_PIN_RESET);
			} else {
				HAL_GPIO_WritePin(MGO, selectPins[j], GPIO_PIN_RESET);
			}
		}
	}

}
